// LEARNING ABOUT FUNCTIONS

// function // name of function // parameters of function to use

function sum (item1, item2) {
    return item1 + item2;
}

function sub (item1, item2) {
    return item1 - item2;
}

// functions only be displayed when we call them

console.log('result:', sum(5,6));
console.log('result:', sub(5,6));
